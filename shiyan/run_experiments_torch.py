DEFAULT_PLOT_LABELS = ["HSK-EM", "Other-HSV", "BK", "FK", "Normal", "NIK"]
RESAMPLING_BILINEAR = Image.Resampling.BILINEAR if hasattr(Image, "Resampling") else Image.BILINEAR


class KeratitisMultimodalDataset(Dataset):
    def __init__(
        self,
        frame: pd.DataFrame,
        text_vectorizer: TfidfVectorizer,
        image_size: int,
        text_column: str = "raw_text_clean",
    ) -> None:
        self.frame = frame.reset_index(drop=True)
        self.text_vectorizer = text_vectorizer
        self.image_size = image_size
        self.text_column = text_column
        self.text_features = self.text_vectorizer.transform(
            self.frame[self.text_column].fillna("").astype(str).tolist()
        ).astype(np.float32)

    def __len__(self) -> int:
        return len(self.frame)

    def _load_modality_stack(self, path_string: str) -> np.ndarray:
        image_paths = [path for path in str(path_string).split(";") if path]
        images = []
        for image_path in image_paths:
            with Image.open(image_path) as image:
                rgb = image.convert("RGB").resize((self.image_size, self.image_size), RESAMPLING_BILINEAR)
                array = np.asarray(rgb, dtype=np.float32) / 255.0
                array = np.transpose(array, (2, 0, 1))
                images.append(array)
        stacked = np.stack(images, axis=0)
        return stacked.mean(axis=0)

    def __getitem__(self, index: int) -> Dict[str, torch.Tensor]:
        row = self.frame.iloc[index]
        dli = self._load_modality_stack(row["dli_paths"])
        fsi = self._load_modality_stack(row["fsi_paths"])
        sbi = self._load_modality_stack(row["sbi_paths"])

        image_tensor = np.concatenate([dli, fsi, sbi], axis=0)
        text_tensor = self.text_features[index].toarray().squeeze(0)
        label = int(row["label_id"])

        return {
            "image": torch.tensor(image_tensor, dtype=torch.float32),
            "text": torch.tensor(text_tensor, dtype=torch.float32),
            "label": torch.tensor(label, dtype=torch.long),
            "sample_id": row["sample_id"],
        }


