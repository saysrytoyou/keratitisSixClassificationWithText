def set_seed(seed: int, device: torch.device) -> None:
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if device.type == "cuda":
        torch.cuda.manual_seed_all(seed)


def collect_cuda_diagnostics() -> dict:
    diagnostics = {
        "torch_version": torch.__version__,
        "torch_cuda_version": torch.version.cuda,
        "cuda_visible_devices": os.environ.get("CUDA_VISIBLE_DEVICES", "<unset>"),
    }
    try:
        diagnostics["cuda_is_available"] = bool(torch.cuda.is_available())
    except Exception as exc:
        diagnostics["cuda_is_available"] = False
        diagnostics["cuda_is_available_error"] = repr(exc)
    try:
        diagnostics["cuda_device_count"] = int(torch.cuda.device_count())
    except Exception as exc:
        diagnostics["cuda_device_count"] = 0
        diagnostics["cuda_device_count_error"] = repr(exc)
    return diagnostics


def format_cuda_diagnostics(diagnostics: dict) -> str:
    ordered_keys = [
        "torch_version",
        "torch_cuda_version",
        "cuda_visible_devices",
        "cuda_is_available",
        "cuda_is_available_error",
        "cuda_device_count",
        "cuda_device_count_error",
    ]
    parts = [f"{key}={diagnostics[key]}" for key in ordered_keys if key in diagnostics]
    return ", ".join(parts)


def get_device(device_name: str, allow_cpu_fallback: bool = False) -> torch.device:
    normalized = device_name.strip().lower()
    if normalized == "cpu":
        return torch.device("cpu")
    if normalized == "cuda":
        target_index = 0
    elif normalized.startswith("cuda:"):
        try:
            target_index = int(normalized.split(":", maxsplit=1)[1])
        except ValueError as exc:
            raise ValueError(f"Invalid CUDA device specifier: {device_name}") from exc
    else:
        return torch.device(device_name)

    diagnostics = collect_cuda_diagnostics()
    device_count = int(diagnostics.get("cuda_device_count", 0))
    if diagnostics.get("cuda_is_available") and device_count > target_index:
        torch.cuda.set_device(target_index)
        selected = torch.device(f"cuda:{target_index}")
        print(f"Using CUDA device cuda:{target_index} ({torch.cuda.get_device_name(target_index)})")
        return selected

    message = (
        f"CUDA was requested as '{device_name}', but it is unavailable or the target index is invalid. "
        f"Diagnostics: {format_cuda_diagnostics(diagnostics)}"
    )
    if allow_cpu_fallback:
        print(message)
        print("Falling back to CPU because --allow-cpu-fallback is set.")
        return torch.device("cpu")
    raise RuntimeError(message)


def read_manifest(path: Path) -> pd.DataFrame:
    if not path.exists():
        raise FileNotFoundError(f"Manifest file not found: {path}")
    df = pd.read_csv(path)
    required_columns = {
        "sample_id",
        "label_id",
        "label_name",
        "raw_text",
        "dli_paths",
        "fsi_paths",
        "sbi_paths",
    }
    missing = required_columns - set(df.columns)
    if missing:
        raise ValueError(f"Manifest is missing required columns: {sorted(missing)}")
    return df


def remap_single_image_path(raw_path: str, picture_dir: Path) -> str:
    candidate = Path(str(raw_path))
    if candidate.exists():
        return str(candidate)

    normalized = str(raw_path).replace("\\", "/")
    parts = [part for part in normalized.split("/") if part and part != "."]
    if "picture" in parts:
        suffix_parts = parts[parts.index("picture") + 1 :]
    else:
        suffix_parts = parts

    remapped = picture_dir.joinpath(*suffix_parts)
    return str(remapped)



def remap_path_string(path_string: str, picture_dir: Path) -> str:
    remapped_paths = []
    for raw_path in [part.strip() for part in str(path_string).split(";") if part.strip()]:
        remapped = remap_single_image_path(raw_path, picture_dir=picture_dir)
        if not Path(remapped).exists():
            raise FileNotFoundError(
                f"Image file not found after manifest remap: original={raw_path}, remapped={remapped}"
            )
        remapped_paths.append(remapped)
    return ";".join(remapped_paths)



def remap_manifest_paths(df: pd.DataFrame, picture_dir: Path) -> pd.DataFrame:
    resolved = df.copy()
    for column in ("dli_paths", "fsi_paths", "sbi_paths"):
        resolved[column] = resolved[column].map(lambda value: remap_path_string(value, picture_dir=picture_dir))
    return resolved



def subset_manifest(df: pd.DataFrame, max_samples_per_class: int | None, seed: int) -> pd.DataFrame:
    if max_samples_per_class is None:
        return df.reset_index(drop=True)
    sampled = []
    for _, group in df.groupby("label_id", sort=True):
        sampled.append(group.sample(n=min(len(group), max_samples_per_class), random_state=seed))
    return pd.concat(sampled, axis=0).sort_values("sample_id").reset_index(drop=True)


def ensure_text_column(df: pd.DataFrame, text_column: str) -> tuple[pd.DataFrame, str]:
    if text_column in df.columns:
        return df, text_column
    if text_column == "raw_text_clean" and "raw_text" in df.columns:
        resolved = df.copy()
        resolved["raw_text_clean"] = resolved["raw_text"].fillna("").astype(str).map(normalize_clinical_text)
        return resolved, "raw_text_clean"
    raise ValueError(f"Manifest is missing text column: {text_column}")


def build_vectorizer(texts: Sequence[str], max_features: int, ngram_min: int, ngram_max: int) -> TfidfVectorizer:
    vectorizer = TfidfVectorizer(
        analyzer="char",
        ngram_range=(ngram_min, ngram_max),
        min_df=1,
        max_features=max_features,
        sublinear_tf=True,
    )
    vectorizer.fit(list(texts))
    return vectorizer


def collate_batch(batch: List[Dict[str, torch.Tensor]]) -> Dict[str, torch.Tensor | List[str]]:
    images = torch.stack([item["image"] for item in batch], dim=0)
    texts = torch.stack([item["text"] for item in batch], dim=0)
    labels = torch.stack([item["label"] for item in batch], dim=0)
    sample_ids = [item["sample_id"] for item in batch]
    return {"image": images, "text": texts, "label": labels, "sample_id": sample_ids}


def compute_epoch_metrics(y_true: Sequence[int], y_pred: Sequence[int], loss_sum: float, count: int) -> EpochMetrics:
    return EpochMetrics(
        loss=float(loss_sum / max(count, 1)),
        accuracy=float(accuracy_score(y_true, y_pred)),
        macro_f1=float(f1_score(y_true, y_pred, average="macro")),
    )


def run_epoch(
    model: nn.Module,
    loader: DataLoader,
    criterion: nn.Module,
    device: torch.device,
    optimizer: torch.optim.Optimizer | None = None,
) -> Tuple[EpochMetrics, np.ndarray, np.ndarray, np.ndarray, List[str]]:
    is_train = optimizer is not None
    model.train(is_train)

    total_loss = 0.0
    total_count = 0
    all_true: List[int] = []
    all_pred: List[int] = []
    all_prob: List[np.ndarray] = []
    all_sample_ids: List[str] = []

    context = torch.enable_grad() if is_train else torch.no_grad()
    with context:
        for batch in loader:
            images = batch["image"].to(device)
            texts = batch["text"].to(device)
            labels = batch["label"].to(device)

            logits = model(images, texts)
            loss = criterion(logits, labels)

            if is_train:
                optimizer.zero_grad()
                loss.backward()
                optimizer.step()

            probs = torch.softmax(logits, dim=1)
            preds = probs.argmax(dim=1)

            batch_size = labels.size(0)
            total_loss += float(loss.item()) * batch_size
            total_count += batch_size
            all_true.extend(labels.detach().cpu().tolist())
            all_pred.extend(preds.detach().cpu().tolist())
            all_prob.append(probs.detach().cpu().numpy())
            all_sample_ids.extend(batch["sample_id"])

    probs_array = np.concatenate(all_prob, axis=0) if all_prob else np.empty((0, 0))
    metrics = compute_epoch_metrics(all_true, all_pred, total_loss, total_count)
    return metrics, np.array(all_true), np.array(all_pred), probs_array, all_sample_ids


def to_plot_labels(labels: Sequence[str]) -> List[str]:
    if len(labels) == len(DEFAULT_PLOT_LABELS):
        return DEFAULT_PLOT_LABELS
    return [f"class_{index}" for index, _ in enumerate(labels)]


def plot_learning_curves(history: Dict[str, List[float]], output_path: Path, title: str) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    epochs = range(1, len(history["train_loss"]) + 1)
    fig, axes = plt.subplots(1, 2, figsize=(12, 4))

    axes[0].plot(epochs, history["train_loss"], label="train_loss")
    axes[0].plot(epochs, history["val_loss"], label="val_loss")
    axes[0].set_title(f"{title} Loss")
    axes[0].set_xlabel("Epoch")
    axes[0].legend()

    axes[1].plot(epochs, history["train_macro_f1"], label="train_macro_f1")
    axes[1].plot(epochs, history["val_macro_f1"], label="val_macro_f1")
    axes[1].set_title(f"{title} Macro-F1")
    axes[1].set_xlabel("Epoch")
    axes[1].legend()

    fig.tight_layout()
    fig.savefig(output_path, dpi=200, bbox_inches="tight")
    plt.close(fig)


def plot_confusion_matrix(cm: np.ndarray, labels: Sequence[str], output_path: Path, title: str) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    plot_labels = to_plot_labels(labels)
    fig, ax = plt.subplots(figsize=(8, 6))
    im = ax.imshow(cm, cmap="Blues")
    ax.set_xticks(range(len(plot_labels)))
    ax.set_yticks(range(len(plot_labels)))
    ax.set_xticklabels(plot_labels, rotation=45, ha="right")
    ax.set_yticklabels(plot_labels)
    ax.set_xlabel("Predicted")
    ax.set_ylabel("True")
    ax.set_title(title)

    for i in range(cm.shape[0]):
        for j in range(cm.shape[1]):
            ax.text(j, i, int(cm[i, j]), ha="center", va="center", color="black", fontsize=9)

    fig.colorbar(im, ax=ax)
    fig.tight_layout()
    fig.savefig(output_path, dpi=220, bbox_inches="tight")
    plt.close(fig)


def plot_per_class_f1(report: Dict[str, Dict[str, float]], labels: Sequence[str], output_path: Path, title: str) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    plot_labels = to_plot_labels(labels)
    f1_values = [report[label]["f1-score"] for label in labels]
    fig, ax = plt.subplots(figsize=(9, 4.5))
    ax.bar(range(len(plot_labels)), f1_values, color="#4C72B0")
    ax.set_xticks(range(len(plot_labels)))
    ax.set_xticklabels(plot_labels, rotation=45, ha="right")
    ax.set_ylim(0.0, 1.0)
    ax.set_ylabel("F1-score")
    ax.set_title(title)
    for idx, value in enumerate(f1_values):
        ax.text(idx, min(value + 0.02, 0.98), f"{value:.2f}", ha="center", va="bottom", fontsize=9)
    fig.tight_layout()
    fig.savefig(output_path, dpi=220, bbox_inches="tight")
    plt.close(fig)


def save_json(path: Path, payload: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


def train_one_fold(
    model_name: str,
    train_df: pd.DataFrame,
    val_df: pd.DataFrame,
    label_names: Sequence[str],
    args: argparse.Namespace,
    device: torch.device,
    fold_id: int,
    output_dir: Path,
    text_column: str,
) -> Tuple[dict, pd.DataFrame]:
    vectorizer = build_vectorizer(
        train_df[text_column].fillna("").astype(str).tolist(),
        max_features=args.text_max_features,
        ngram_min=args.text_ngram_min,
        ngram_max=args.text_ngram_max,
    )
    train_dataset = KeratitisMultimodalDataset(
        train_df,
        vectorizer,
        image_size=args.image_size,
        text_column=text_column,
    )
    val_dataset = KeratitisMultimodalDataset(
        val_df,
        vectorizer,
        image_size=args.image_size,
        text_column=text_column,
    )

    train_loader = DataLoader(
        train_dataset,
        batch_size=args.batch_size,
        shuffle=True,
        num_workers=args.num_workers,
        pin_memory=(device.type == "cuda"),
        collate_fn=collate_batch,
    )
    val_loader = DataLoader(
        val_dataset,
        batch_size=args.batch_size,
        shuffle=False,
        num_workers=args.num_workers,
        pin_memory=(device.type == "cuda"),
        collate_fn=collate_batch,
    )

    text_dim = len(vectorizer.get_feature_names_out())
    model = build_model(
        model_name=model_name,
        text_dim=text_dim,
        num_classes=len(label_names),
        feature_dim=args.feature_dim,
    ).to(device)
    class_weights = None
    if args.use_class_weights:
        label_counts = train_df["label_id"].value_counts().sort_index()
        total_count = float(label_counts.sum())
        class_weights = np.array(
            [total_count / (len(label_names) * float(label_counts.get(class_id, 1))) for class_id in range(len(label_names))],
            dtype=np.float32,
        )
    criterion = nn.CrossEntropyLoss(
        weight=torch.tensor(class_weights, dtype=torch.float32, device=device) if class_weights is not None else None
    )
    optimizer = torch.optim.Adam(model.parameters(), lr=args.lr, weight_decay=args.weight_decay)
    scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(
        optimizer,
        mode="max",
        factor=args.lr_scheduler_factor,
        patience=args.lr_scheduler_patience,
    )

    history = {
        "train_loss": [],
        "val_loss": [],
        "train_macro_f1": [],
        "val_macro_f1": [],
        "train_accuracy": [],
        "val_accuracy": [],
        "lr": [],
    }

    best_state = None
    best_val_f1 = -math.inf
    best_val_outputs = None
    epochs_without_improvement = 0

    for epoch_index in range(args.epochs):
        train_metrics, _, _, _, _ = run_epoch(model, train_loader, criterion, device, optimizer=optimizer)
        val_metrics, y_true, y_pred, probs, sample_ids = run_epoch(model, val_loader, criterion, device, optimizer=None)
        scheduler.step(val_metrics.macro_f1)

        history["train_loss"].append(train_metrics.loss)
        history["val_loss"].append(val_metrics.loss)
        history["train_macro_f1"].append(train_metrics.macro_f1)
        history["val_macro_f1"].append(val_metrics.macro_f1)
        history["train_accuracy"].append(train_metrics.accuracy)
        history["val_accuracy"].append(val_metrics.accuracy)
        history["lr"].append(float(optimizer.param_groups[0]["lr"]))

        if val_metrics.macro_f1 > best_val_f1:
            best_val_f1 = val_metrics.macro_f1
            best_state = {key: value.detach().cpu() for key, value in model.state_dict().items()}
            best_val_outputs = (y_true.copy(), y_pred.copy(), probs.copy(), list(sample_ids))
            epochs_without_improvement = 0
        else:
            epochs_without_improvement += 1

        if (epoch_index + 1) >= args.min_epochs and epochs_without_improvement >= args.early_stop_patience:
            break

    if best_state is None or best_val_outputs is None:
        raise RuntimeError("Training did not produce a valid best checkpoint.")

    model.load_state_dict(best_state)
    y_true, y_pred, probs, sample_ids = best_val_outputs

    labels = list(range(len(label_names)))
    report = classification_report(
        y_true,
        y_pred,
        labels=labels,
        target_names=label_names,
        output_dict=True,
        zero_division=0,
    )
    cm = confusion_matrix(y_true, y_pred, labels=labels)

    model_dir = output_dir / model_name / f"fold_{fold_id}"
    model_dir.mkdir(parents=True, exist_ok=True)
    torch.save(best_state, model_dir / "best_model.pt")

    plot_learning_curves(history, model_dir / "learning_curve.png", f"{model_name} fold {fold_id}")
    plot_confusion_matrix(cm, label_names, model_dir / "confusion_matrix.png", f"{model_name} fold {fold_id}")
    plot_per_class_f1(report, label_names, model_dir / "per_class_f1.png", f"{model_name} fold {fold_id}")

    fold_metrics = {
        "fold_id": fold_id,
        "accuracy": float(accuracy_score(y_true, y_pred)),
        "macro_f1": float(f1_score(y_true, y_pred, average="macro")),
        "classification_report": report,
        "confusion_matrix": cm.tolist(),
        "history": history,
        "num_train": int(len(train_df)),
        "num_val": int(len(val_df)),
        "best_epoch": int(np.argmax(history["val_macro_f1"]) + 1),
        "text_dim": int(text_dim),
        "text_column": text_column,
        "class_weights": class_weights.tolist() if class_weights is not None else None,
    }
    save_json(model_dir / "metrics.json", fold_metrics)

    pred_frame = pd.DataFrame(
        {
            "sample_id": sample_ids,
            "fold_id": fold_id,
            "model_name": model_name,
            "y_true": y_true,
            "y_pred": y_pred,
        }
    )
    for class_id, label_name in enumerate(label_names):
        pred_frame[f"prob_{class_id}_{label_name}"] = probs[:, class_id]
    return fold_metrics, pred_frame


def summarize_all_results(
    all_metrics: Dict[str, List[dict]],
    label_names: Sequence[str],
    args: argparse.Namespace,
    device: torch.device,
    text_column: str,
) -> dict:
    summary = {}
    for model_name, fold_metrics in all_metrics.items():
        accuracies = [item["accuracy"] for item in fold_metrics]
        macro_f1s = [item["macro_f1"] for item in fold_metrics]
        summary[model_name] = {
            "mean_accuracy": float(np.mean(accuracies)),
            "std_accuracy": float(np.std(accuracies)),
            "mean_macro_f1": float(np.mean(macro_f1s)),
            "std_macro_f1": float(np.std(macro_f1s)),
            "num_folds": len(fold_metrics),
            "labels": list(label_names),
        }
    summary["config"] = {
        "manifest": str(args.manifest.resolve()),
        "output_dir": str(args.output_dir.resolve()),
        "picture_dir": str(args.picture_dir.resolve()),
        "models": args.models,
        "cv_folds": args.cv_folds,
        "epochs": args.epochs,
        "batch_size": args.batch_size,
        "num_workers": args.num_workers,
        "lr": args.lr,
        "weight_decay": args.weight_decay,
        "image_size": args.image_size,
        "text_max_features": args.text_max_features,
        "text_column": text_column,
        "text_ngram_min": args.text_ngram_min,
        "text_ngram_max": args.text_ngram_max,
        "feature_dim": args.feature_dim,
        "seed": args.seed,
        "max_samples_per_class": args.max_samples_per_class,
        "device": str(device),
        "requested_device": args.device,
        "allow_cpu_fallback": args.allow_cpu_fallback,
        "cuda_available": bool(torch.cuda.is_available()),
        "cuda_name": torch.cuda.get_device_name(0) if torch.cuda.is_available() else None,
        "use_class_weights": args.use_class_weights,
        "min_epochs": args.min_epochs,
        "early_stop_patience": args.early_stop_patience,
        "lr_scheduler_patience": args.lr_scheduler_patience,
        "lr_scheduler_factor": args.lr_scheduler_factor,
    }
    return summary


def main() -> None:
    args = parse_args()
    device = get_device(args.device, allow_cpu_fallback=args.allow_cpu_fallback)
    set_seed(args.seed, device)

    df = read_manifest(args.manifest.resolve())
    df = remap_manifest_paths(df, picture_dir=args.picture_dir.resolve())
    df, text_column = ensure_text_column(df, args.text_column)
    df = subset_manifest(df, max_samples_per_class=args.max_samples_per_class, seed=args.seed)

    label_map = df[["label_id", "label_name"]].drop_duplicates().sort_values("label_id").reset_index(drop=True)
    label_names = label_map["label_name"].astype(str).tolist()
    y = df["label_id"].astype(int).to_numpy()

    splitter = StratifiedKFold(n_splits=args.cv_folds, shuffle=True, random_state=args.seed)

    all_metrics: Dict[str, List[dict]] = {name: [] for name in args.models}
    all_predictions: List[pd.DataFrame] = []

    output_dir = args.output_dir.resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    for model_name in args.models:
        print(f"Running model={model_name} on device={device}")
        for fold_id, (train_idx, val_idx) in enumerate(splitter.split(df, y), start=1):
            train_df = df.iloc[train_idx].reset_index(drop=True)
            val_df = df.iloc[val_idx].reset_index(drop=True)
            fold_metrics, pred_frame = train_one_fold(
                model_name=model_name,
                train_df=train_df,
                val_df=val_df,
                label_names=label_names,
                args=args,
                device=device,
                fold_id=fold_id,
                output_dir=output_dir,
                text_column=text_column,
            )
            all_metrics[model_name].append(fold_metrics)
            all_predictions.append(pred_frame)

    summary = summarize_all_results(all_metrics, label_names, args, device, text_column)
    save_json(output_dir / "summary.json", summary)

    for model_name, metrics_list in all_metrics.items():
        save_json(output_dir / f"{model_name}_fold_metrics.json", {"folds": metrics_list})

    if all_predictions:
        pd.concat(all_predictions, axis=0).reset_index(drop=True).to_csv(
            output_dir / "predictions.csv", index=False, encoding="utf-8-sig"
        )

    summary_rows = []
    for model_name, model_summary in summary.items():
        if model_name == "config":
            continue
        summary_rows.append(
            {
                "model_name": model_name,
                "mean_accuracy": model_summary["mean_accuracy"],
                "std_accuracy": model_summary["std_accuracy"],
                "mean_macro_f1": model_summary["mean_macro_f1"],
                "std_macro_f1": model_summary["std_macro_f1"],
            }
        )
    if summary_rows:
        pd.DataFrame(summary_rows).to_csv(output_dir / "summary_table.csv", index=False, encoding="utf-8-sig")

    print(json.dumps(summary, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
